# Find ASCII Value of Character


c = str(input("Enter character: "))
print("The ASCII value of '" + c + "' is", ord(c))

# Output:
# Enter character: @
# The ASCII value of '@' is 64

# Enter character: a
# The ASCII value of 'a' is 97

# Enter character: |  
# The ASCII value of '|' is 124
