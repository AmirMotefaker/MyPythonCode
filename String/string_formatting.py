# String Formatting

color = 'yellow'
print("{name}'s {object} is {color}".format(color = "blue", object = "bag", name = "john"))
# john's bag is blue

print("My score is {score:10.3f}". format(score = 10 / 3))
# My score is      3.333


# f-strings
name = 'John'
item = 'car'
color = 'blue'

print(f"{name}'s {item} is {color}.")
# John's car is blue.

